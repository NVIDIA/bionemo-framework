"""Split-aware memmap dataset for CodonFM pretraining.

Ports the index construction, sequence-cluster filtering, and proportional cluster split
from the PTL recipe (codonfm_ptl_te) so the native_te recipe sees the same train/val/test
sample sets when pointed at the same data directory and seed. On-disk cache files
(metadata.cache*.npy, train_idx*.npy, val_idx*.npy, test_idx*.npy) use PTL's filename
conventions, so caches generated by either recipe are interchangeable.

A cache_params*.json sidecar is written on regeneration to detect silent parameter drift.
"""

import hashlib
import json
import logging
from pathlib import Path

import numpy as np
import torch.distributed as dist
from torch.utils.data import Dataset
from tqdm import tqdm


logger = logging.getLogger(__name__)


_VALID_SPLITS = ("train", "validation", "test")


def _is_distributed() -> bool:
    """True when torch.distributed has been initialized for the current process."""
    return dist.is_available() and dist.is_initialized()


def _is_rank_zero() -> bool:
    """True on global rank 0 under torch.distributed, or unconditionally in single-process runs."""
    return not _is_distributed() or dist.get_rank() == 0


def _barrier() -> None:
    """Synchronize all ranks if distributed is initialized; no-op otherwise."""
    if _is_distributed():
        dist.barrier()


def _log_info_rank0(msg: str, *args) -> None:
    """Emit logger.info on rank 0 only; no-op on other ranks. Prevents N-way log duplication."""
    if _is_rank_zero():
        logger.info(msg, *args)


def _sample_clusters_by_size(count_mat, global_keys, used_clusters=None, target_size=30000, seed=42):
    """Verbatim port of codonfm_ptl_te.src.data.dataset_utils.sample_clusters_by_size.

    Sample whole sequence-clusters proportionally per organism group, starting from rarest,
    until target_size sequences are reached. Used to carve val/test out of global_keys.
    """
    if used_clusters is None:
        used_clusters = []

    np.random.seed(seed)  # noqa: NPY002 -- verbatim port; PTL uses NumPy global RNG here

    total_per_organism = count_mat.sum(axis=0)
    total_sum = total_per_organism.sum()
    if total_sum == 0:
        raise ValueError("No sequences found in count matrix")
    target_proportions = total_per_organism / total_sum
    org_indices = np.argsort(total_per_organism)

    selected_clusters = []
    current_counts = np.zeros_like(total_per_organism)
    current_total = 0

    for org_idx in tqdm(org_indices, desc="Sampling clusters"):
        target_count = max(1, int(target_size * target_proportions[org_idx]))
        valid_clusters = np.where(count_mat[:, org_idx] > 0)[0]
        valid_clusters = list(set(valid_clusters) - set(selected_clusters) - set(used_clusters))
        np.random.shuffle(valid_clusters)  # noqa: NPY002

        for cluster_idx in valid_clusters:
            if current_counts[org_idx] >= target_count:
                break
            selected_clusters.append(cluster_idx)
            current_counts += count_mat[cluster_idx]
            current_total += count_mat[cluster_idx].sum()

    selected_indices = np.where(np.isin(global_keys, selected_clusters))[0]
    return np.array(selected_indices), selected_clusters


def _load_train_val_test_indices_proportional(
    metadata_path,
    global_keys,
    global_indices,
    post_fix="",
    train_val_test_ratio=(0.9998, 0.0002, 0.0),
    seed=42,
    force_recompute=False,
):
    """Verbatim port of codonfm_ptl_te.src.data.dataset_utils.load_train_val_test_indices_proportional.

    When force_recompute=True the existing train_idx/val_idx/test_idx files (if any) are ignored
    and overwritten with freshly computed splits. Callers must set this to True whenever the
    upstream global_indices has just been regenerated, otherwise stale splits would silently
    pair with a fresh global array.
    """
    train_idx_path = metadata_path.parent / f"train_idx{post_fix}.npy"
    val_idx_path = metadata_path.parent / f"val_idx{post_fix}.npy"
    test_idx_path = metadata_path.parent / f"test_idx{post_fix}.npy"

    cache_hit = not force_recompute and train_idx_path.exists() and val_idx_path.exists() and test_idx_path.exists()

    # Synchronize the cache-presence decision before any rank writes. Without this barrier a
    # straggler rank could check existence AFTER rank 0 wrote the files and disagree on the
    # branch, leading to a downstream barrier deadlock (one rank in regen, others in cache-hit).
    _barrier()

    if not cache_hit:
        if _is_rank_zero():
            _log_info_rank0("Computing train/val/test split indices (post_fix=%r)", post_fix)
            with open(metadata_path, "r") as f:
                metadata = json.load(f)["file_metadata"]
            groups = sorted({metadata[i]["file_name"].split(".")[0] for i in range(len(metadata))})
            count_mat = np.zeros((max(global_keys) + 1, len(groups)))
            for idx, cluster_idx in zip(global_indices, global_keys):
                group = metadata[idx[0]]["file_name"].split(".")[0]
                count_mat[cluster_idx, groups.index(group)] += 1

            val_size = int(len(global_keys) * train_val_test_ratio[1])
            test_size = int(len(global_keys) * train_val_test_ratio[2])
            val_indices, val_clusters = np.array([]), []
            test_indices, test_clusters = np.array([]), []
            if val_size > 0:
                val_indices, val_clusters = _sample_clusters_by_size(
                    count_mat, global_keys, used_clusters=[], target_size=val_size, seed=seed
                )
            if test_size > 0:
                test_indices, test_clusters = _sample_clusters_by_size(
                    count_mat, global_keys, used_clusters=val_clusters, target_size=test_size, seed=seed
                )
            train_indices = np.setdiff1d(np.arange(len(global_keys)), np.concatenate([val_indices, test_indices]))
            np.save(train_idx_path, train_indices)
            np.save(val_idx_path, val_indices)
            np.save(test_idx_path, test_indices)
        # Non-rank-0 ranks (and rank 0 after writing) wait here so everyone loads the same
        # on-disk state below.
        _barrier()
    else:
        _log_info_rank0("Loading existing train/val/test split indices from disk (post_fix=%r)", post_fix)

    train_indices = np.load(train_idx_path, mmap_mode="r")
    val_indices = np.load(val_idx_path, mmap_mode="r")
    test_indices = np.load(test_idx_path, mmap_mode="r")
    return train_indices, val_indices, test_indices


def _get_taxids_to_exclude(taxid_exclusion_file):
    """Load taxids-to-exclude JSON. Verbatim port of PTL's get_taxids_to_exclude."""
    with open(taxid_exclusion_file, "r") as f:
        exclusion_content = json.load(f)
    taxids = []
    for v in exclusion_content.values():
        taxids.extend(v)
    return set(taxids)


def _taxid_exclusion_hash(taxids_to_exclude: set | None) -> str | None:
    """Stable SHA-256 hash of the sorted excluded-taxid set, or None when no exclusion is in effect.

    Hashes the parsed set rather than the raw file bytes, so the stamp is insensitive to JSON
    formatting and key ordering while still catching content changes that affect which sequences
    appear in global_indices. JSON-serializes the sorted int-coerced taxids for a deterministic
    representation that does not depend on numpy/Python version-specific repr().
    """
    if taxids_to_exclude is None:
        return None
    payload = json.dumps(sorted(int(t) for t in taxids_to_exclude), separators=(",", ":"))
    return hashlib.sha256(payload.encode("utf-8")).hexdigest()


def _parity_stamp(**args) -> dict:
    """Stable JSON-serializable dict of parity-affecting args (used to detect silent cache drift)."""
    return {k: args[k] for k in sorted(args)}


class CodonMemmapDataset(Dataset):
    """Split-aware memmap dataset that reproduces the PTL recipe's sample selection.

    Each instance is bound to a single split ("train", "validation", or "test"). The class reads the
    memmap format produced by codonfm_ptl_te's preprocessing (a directory with metadata.json plus
    sequences_chunk*.mmap and index_chunk*.mmap files) and exposes the same per-window slices the
    PTL recipe trains on, filtered by the same min/max length and taxid rules, and split via the
    same proportional cluster sampler.

    Cache files follow PTL's filename conventions, but the strict contract here is that a
    cache is only considered valid when the metadata.cache_params{cache_suffix}.json sidecar
    is also present. An unstamped cache (e.g. one produced by codonfm_ptl_te, which does not
    write stamps) is treated as missing and triggers a full rebuild. The stamp records every
    parity-affecting argument and is namespaced with cache_suffix (= "_{split_name_prefix}"),
    so it shares a namespace with cache_path/key_cache_path and catches context_overlap drift.

    Args:
        data_path: Path to directory containing metadata.json and mmap chunk files.
        split: Which split this instance is bound to ("train", "validation", "test").
        max_seq_length: Model context length including special tokens (PTL calls this context_length).
        context_overlap: Overlap between consecutive windows of the same source sequence (token count).
        pretraining_task: "mlm" (reserves 2 tokens for CLS/SEP) or "next_token_prediction" (reserves 1).
        train_val_test_ratio: Three-element tuple summing to <=1; passes through to the cluster sampler.
        min_seq_length: Drop source sequences with fewer than this many tokens.
        max_filter_seq_length: Drop source sequences with more than this many tokens.
        groups_to_use: Optional list of organism group names to restrict to (intersected per split).
        taxid_exclusion_file: Optional JSON file listing taxids to drop.
        split_name_prefix: Suffix appended to cache filenames for namespacing (required if
            taxid_exclusion_file is set, since exclusion changes which windows exist).
        seed: Random seed for the proportional cluster split.
        force_recompute: If True, ignore any existing cache (the global window cache AND the
            per-split index files) and rebuild from scratch. The flag cascades from the global
            cache into the split loader so a freshly regenerated global_indices is never paired
            with stale on-disk splits.

    Note:
        __getitem__ returns {"sequence_tokens": np.ndarray} carrying the raw memmap codon
        IDs — NOT a decoded codon string. Returning IDs directly preserves <UNK> tokens
        (ID 2) that the existing MemmapCodonDataset silently drops via a decode -> string ->
        re-encode round trip (the codon tokenizer's tokenize() is strict 3-char chunking
        that misaligns when "<UNK>" literals appear in the decoded string). The
        CodonMLMCollator / CodonTHDCollator branch on the key name and wrap with CLS/SEP
        without re-tokenizing.
    """

    def __init__(
        self,
        data_path: str,
        *,
        split: str = "train",
        max_seq_length: int = 2048,
        context_overlap: int = 0,
        pretraining_task: str = "mlm",
        train_val_test_ratio: tuple[float, float, float] = (0.9998, 0.0002, 0.0),
        min_seq_length: int = 100,
        max_filter_seq_length: int = 150_000,
        groups_to_use: list[str] | None = None,
        taxid_exclusion_file: str | None = None,
        split_name_prefix: str = "",
        seed: int = 123,
        force_recompute: bool = False,
    ):
        """Initialize."""
        if split not in _VALID_SPLITS:
            raise ValueError(f"split must be one of {_VALID_SPLITS}, got {split!r}")

        self.data_path = Path(data_path)
        self.metadata_path = self.data_path / "metadata.json"
        self.split = split
        self.max_seq_length = max_seq_length
        self.context_overlap = context_overlap
        self.pretraining_task = pretraining_task
        self.train_val_test_ratio = tuple(train_val_test_ratio)
        self.min_seq_length = min_seq_length
        self.max_filter_seq_length = max_filter_seq_length
        self.groups_to_use = groups_to_use
        self.taxid_exclusion_file = taxid_exclusion_file
        self.split_name_prefix = split_name_prefix
        self.seed = seed

        # PTL convention: MLM reserves CLS+SEP (2), NTP reserves a single sentinel (1).
        # The collator will reinsert these at batch time; we just leave room in each window.
        if pretraining_task == "mlm":
            self.tok_adjust = 2
        elif pretraining_task == "next_token_prediction":
            self.tok_adjust = 1
            raise ValueError("next_token_prediction is not supported for codonfm_native_te")
        else:
            raise ValueError(f"Invalid pretraining_task '{pretraining_task}'")

        self.taxids_to_exclude = None
        if taxid_exclusion_file:
            if not split_name_prefix:
                raise ValueError("split_name_prefix is required when taxid_exclusion_file is set")
            self.taxids_to_exclude = _get_taxids_to_exclude(taxid_exclusion_file)
            _log_info_rank0("Loaded %d taxids to exclude", len(self.taxids_to_exclude))

        with open(self.metadata_path, "r") as f:
            self.metadata = json.load(f)

        self.sequences_mmaps = []
        self.indices_mmaps = []
        for chunk in self.metadata["chunks"]:
            seq_mmap = np.memmap(
                self.data_path / chunk["sequences"]["path"],
                dtype=chunk["sequences"]["dtype"],
                mode="r",
                shape=tuple(chunk["sequences"]["shape"]),
            )
            idx_mmap = np.memmap(
                self.data_path / chunk["index"]["path"],
                dtype=chunk["index"]["dtype"],
                mode="r",
                shape=tuple(chunk["index"]["shape"]),
            )
            self.sequences_mmaps.append(seq_mmap)
            self.indices_mmaps.append(idx_mmap)

        # PTL filename conventions: cache_suffix gates the all-windows cache, post_fix gates the
        # per-split cache. They diverge because PTL only namespaced splits (not the underlying
        # windows) by overlap. We preserve that scheme so PTL-generated caches are readable.
        cache_suffix = f"_{split_name_prefix}" if split_name_prefix else ""
        post_fix = "_" + split_name_prefix if split_name_prefix else ""
        if context_overlap != 0:
            post_fix = post_fix + f"_overlap_{context_overlap}"

        cache_path = self.metadata_path.with_suffix(f".cache{cache_suffix}.npy")
        key_cache_path = self.metadata_path.with_suffix(f".key.cache{cache_suffix}.npy")
        stamp_path = self.metadata_path.with_suffix(f".cache_params{cache_suffix}.json")

        current_stamp = _parity_stamp(
            max_seq_length=max_seq_length,
            context_overlap=context_overlap,
            pretraining_task=pretraining_task,
            train_val_test_ratio=list(train_val_test_ratio),
            min_seq_length=min_seq_length,
            max_filter_seq_length=max_filter_seq_length,
            groups_to_use=sorted(groups_to_use) if groups_to_use else None,
            taxid_exclusion_hash=_taxid_exclusion_hash(self.taxids_to_exclude),
            split_name_prefix=split_name_prefix,
            seed=seed,
        )

        # Strict contract: a cache is only considered present when ALL three files
        # (globals + keys + stamp) exist together. An unstamped cache (e.g. one produced by
        # codonfm_ptl_te) is treated as invalid and triggers a full rebuild.
        cache_present = cache_path.exists() and key_cache_path.exists() and stamp_path.exists()
        regenerated = (not cache_present) or force_recompute

        # Stamp validation is a pure read; every rank reads the same on-disk file and either
        # raises consistently or proceeds. Done before the decision-sync barrier so the
        # mismatch error surfaces immediately on all ranks.
        if cache_present and not force_recompute:
            with open(stamp_path, "r") as f:
                on_disk_stamp = json.load(f)
            if on_disk_stamp != current_stamp:
                raise RuntimeError(
                    f"Cache parity-stamp at {stamp_path} disagrees with current arguments. "
                    f"Set force_recompute=True to regenerate.\n"
                    f"On-disk:  {on_disk_stamp}\nCurrent:  {current_stamp}"
                )

        # Synchronize the cache-presence decision before any rank writes. Without this barrier a
        # straggler rank could check existence AFTER rank 0 wrote the globals and disagree on the
        # branch, leading to a downstream barrier deadlock (one rank in regen, others in cache-hit).
        _barrier()

        if regenerated:
            if _is_rank_zero():
                # Invalidate the old stamp before touching anything else. A crash anywhere
                # between here and the stamp rewrite at the end leaves "no stamp on disk",
                # which the next run treats as cache invalid -> full rebuild.
                stamp_path.unlink(missing_ok=True)
                _log_info_rank0("Computing global indices for subsequences (this may take a while)")
                global_indices_arr, global_keys_arr = self._build_global_indices()
                np.save(cache_path, global_indices_arr)
                np.save(key_cache_path, global_keys_arr)
            # Non-rank-0 ranks (and rank 0 after writing) wait here so everyone loads the same
            # on-disk state below.
            _barrier()

        # Every rank loads as mmap views so train+val instances share kernel page cache.
        self.global_indices = np.load(cache_path, mmap_mode="r")
        self.global_keys = np.load(key_cache_path, mmap_mode="r")

        # Cascade the recompute into the split loader: whenever globals were rebuilt (explicit
        # force_recompute or cache miss), the on-disk split files may have been written against a
        # different global_indices and must be overwritten — otherwise stale splits silently pair
        # with fresh global arrays. The split loader handles its own rank-0 gating internally.
        train_indices, val_indices, test_indices = _load_train_val_test_indices_proportional(
            metadata_path=self.metadata_path,
            global_keys=self.global_keys,
            global_indices=self.global_indices,
            post_fix=post_fix,
            train_val_test_ratio=self.train_val_test_ratio,
            seed=seed,
            force_recompute=regenerated,
        )

        n_total = len(train_indices) + len(val_indices) + len(test_indices)
        if n_total != len(self.global_indices):
            raise RuntimeError(
                f"Split indices do not partition global_indices (size mismatch): "
                f"{len(train_indices)} + {len(val_indices)} + {len(test_indices)} != {len(self.global_indices)}"
            )
        # Size match alone misses overlaps balanced by gaps; verify uniqueness and range for loaded caches.
        all_indices = np.concatenate([train_indices, val_indices, test_indices])
        if len(np.unique(all_indices)) != n_total:
            raise RuntimeError("Split indices contain duplicates across train/val/test")
        if n_total > 0 and (all_indices.min() < 0 or all_indices.max() >= len(self.global_indices)):
            raise RuntimeError(
                f"Split indices out of range [0, {len(self.global_indices)}): "
                f"min={all_indices.min()}, max={all_indices.max()}"
            )

        # Stamp written only after the partition check passes, so a construction-bug failure
        # leaves no stamp on disk falsely blessing the bad cache.
        if regenerated:
            if _is_rank_zero():
                with open(stamp_path, "w") as f:
                    json.dump(current_stamp, f, indent=2, sort_keys=True)
            _barrier()

        if groups_to_use:
            groups = [
                self.metadata["file_metadata"][i]["file_name"].split(".")[0]
                for i in range(len(self.metadata["file_metadata"]))
            ]
            missing = set(groups_to_use) - set(groups)
            if missing:
                raise ValueError(f"Groups not present in dataset: {sorted(missing)}. Available: {groups}")
            global_groups = [groups[i] for i in self.global_indices[:, 0]]
            indices_to_use = np.where(np.isin(global_groups, groups_to_use))[0]
            train_indices = np.intersect1d(train_indices, indices_to_use)
            val_indices = np.intersect1d(val_indices, indices_to_use)
            test_indices = np.intersect1d(test_indices, indices_to_use)

        if len(train_indices) == 0 or len(val_indices) == 0:
            raise ValueError("Empty train or validation split after filtering")

        split_map = {"train": train_indices, "validation": val_indices, "test": test_indices}
        self.split_indices = split_map[split]

        _log_info_rank0(
            "CodonMemmapDataset(split=%s) ready: %d windows in this split / %d total",
            split,
            len(self.split_indices),
            len(self.global_indices),
        )

    def _build_global_indices(self) -> tuple[np.ndarray, np.ndarray]:
        """Build the full per-window index from the chunk memmaps.

        Verbatim port of codonfm_ptl_te.src.data.codon_memmap_dataset.CodonMemmapDataset's
        inner loop, including filtering by taxid / min_seq_length / max_filter_seq_length and
        the floor-division windowing (which can drop a partial tail window vs. the existing
        MemmapCodonDataset's ceiling-division windowing).

        Returns:
            (global_indices, global_keys) — int64 arrays of shape (num_windows, 3) and (num_windows,).
        """
        global_indices_list = []
        global_keys = []
        total_sequences = sum(len(idx_mmap) for idx_mmap in self.indices_mmaps)

        seq_cluster_path = self.data_path / "allSeqClusterIdx.npy"
        if seq_cluster_path.exists():
            seq_clusters = np.load(seq_cluster_path, allow_pickle=True)
            if seq_clusters.shape[0] != total_sequences:
                raise RuntimeError(
                    f"Mismatch: allSeqClusterIdx.npy has {seq_clusters.shape[0]} entries, "
                    f"but total_sequences across chunks is {total_sequences}"
                )
        else:
            # Without a cluster file every sequence is its own cluster; the proportional
            # sampler then degrades to per-sequence sampling.
            seq_clusters = np.arange(total_sequences, dtype=int)

        step_size = self.max_seq_length - self.tok_adjust - self.context_overlap
        global_seq_idx = 0
        with tqdm(total=total_sequences, desc="Processing sequences") as pbar:
            for chunk_id, idx_mmap in enumerate(self.indices_mmaps):
                for seq_idx in range(len(idx_mmap)):
                    seq_cluster_idx = seq_clusters[global_seq_idx]
                    global_seq_idx += 1
                    seq_start, seq_end, taxid = idx_mmap[seq_idx]
                    if self.taxids_to_exclude and taxid in self.taxids_to_exclude:
                        pbar.update(1)
                        continue
                    seq_len_tokens = seq_end - seq_start
                    if seq_len_tokens < self.min_seq_length or seq_len_tokens > self.max_filter_seq_length:
                        pbar.update(1)
                        continue
                    num_subsequences = max(1, (seq_len_tokens - self.context_overlap) // step_size)
                    for sub_seq_idx in range(num_subsequences):
                        start_token_idx = seq_start + sub_seq_idx * step_size
                        end_token_idx = min(start_token_idx + (self.max_seq_length - self.tok_adjust), seq_end)
                        if end_token_idx > start_token_idx:
                            global_indices_list.append([chunk_id, start_token_idx, end_token_idx])
                            global_keys.append(seq_cluster_idx)
                    pbar.update(1)

        return (
            np.array(global_indices_list, dtype=np.int64),
            np.array(global_keys, dtype=np.int64),
        )

    def __len__(self) -> int:  # noqa: D105
        return len(self.split_indices)

    def __getitem__(self, idx: int) -> dict[str, np.ndarray]:  # noqa: D105
        global_idx = self.split_indices[idx]
        chunk_id, start, end = self.global_indices[global_idx]
        return {"sequence_tokens": np.array(self.sequences_mmaps[chunk_id][start:end])}
